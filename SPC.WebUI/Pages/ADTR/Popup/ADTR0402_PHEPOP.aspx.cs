        {
            string errMsg = String.Empty;

            using (ADTRBiz biz = new ADTRBiz())
            {
                oParamDic.Clear();
                oParamDic.Add("F_COMPCD", gsCOMPCD);
                oParamDic.Add("F_FACTCD", gsFACTCD);
                oParamDic.Add("F_MEASNO", keyFields[0]);
                oParamDic.Add("F_TSERIALNO", keyFields[1]);

                ds = biz.ADTR0402_PHE_LST(oParamDic, out errMsg);
            }

            //ds.Tables[0].Rows[0]["F_REMARK"] = ds.Tables[0].Rows[0]["F_REMARK"].ToString().Replace("\r\n", " <br />");

            devGrid.DataSource = ds;


            if (!String.IsNullOrEmpty(errMsg))
            {
                // Grid Callback Init
                devGrid.JSProperties["cpResultCode"] = "0";
                devGrid.JSProperties["cpResultMsg"] = errMsg;
            }
            else
            {
                if (IsCallback)
                    devGrid.DataBind();
            }
        }
