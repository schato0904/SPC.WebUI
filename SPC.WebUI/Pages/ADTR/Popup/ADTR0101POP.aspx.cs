        #region 라인모니터링 팝업 조회
        void QCDPCNM_ADTR0101_POP()
        {
            string errMsg = String.Empty;

            DateTime dtNow = DateTime.Now;
            int Time = dtNow.Hour;

            string strFromdt = oParams[2];
            string strTodt = Convert.ToDateTime(oParams[3]).AddDays(1).ToString("yyyy-MM-dd");
            if (Time < 8)
            {
                strFromdt = Convert.ToDateTime(oParams[2]).AddDays(-1).ToString("yyyy-MM-dd");
                strTodt = oParams[3];
            }

            using (ADTRBiz biz = new ADTRBiz())
            {
                oParamDic.Clear();
                oParamDic.Add("F_COMPCD", oParams[0]);
                oParamDic.Add("F_FACTCD", oParams[1]);
                oParamDic.Add("F_FROMDT", strFromdt);
                oParamDic.Add("F_TODT", strTodt);
                oParamDic.Add("F_PCNM", oParams[4]);

                ds = biz.QCDPCNM_ADTR0101_POP(oParamDic, out errMsg);
            }

            devGrid.DataSource = ds;

            if (!String.IsNullOrEmpty(errMsg))
            {
                // Grid Callback Init
                devGrid.JSProperties["cpResultCode"] = "0";
                devGrid.JSProperties["cpResultMsg"] = errMsg;
            }
            else
            {
                if (IsCallback)
                    devGrid.DataBind();
            }
        }
        #endregion

