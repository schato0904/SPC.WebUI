            {
                oParamDic.Clear();
                oParamDic.Add("F_COMPCD", gsCOMPCD);
                oParamDic.Add("F_FACTCD", gsFACTCD);
                oParamDic.Add("F_MACHCD", keyFields[0]);

                ds = biz.USP_PLCM3002POP_MACHNM_LST(oParamDic, out errMsg);
            }
            txtMACHNM.Text = ds.Tables[0].Rows[0]["F_MACHNM"].ToString();
        }
