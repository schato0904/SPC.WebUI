        #region 사용자 정의 함수

        #region 업무지시목록
        void WORK01_LST()
        {
            string errMsg = String.Empty;

            using (BSIFBiz biz = new BSIFBiz())
            {
                oParamDic.Clear();
                oParamDic.Add("F_COMPCD", gsCOMPCD);
                oParamDic.Add("F_FACTCD", gsFACTCD);

                ds = biz.WORK01_LST(oParamDic, out errMsg);
            }

            devGrid.DataSource = ds;

            if (!String.IsNullOrEmpty(errMsg))
            {
                // Grid Callback Init
                devGrid.JSProperties["cpResultCode"] = "0";
                devGrid.JSProperties["cpResultMsg"] = errMsg;
            }
            else
            {
                if (IsCallback)
                    devGrid.DataBind();
            }
        }
        #endregion

        #endregion

        #region devGrid_CustomCallback

        #region devGrid_CustomCallback
        /// <summary>
        /// devGrid_CustomCallback
        /// </summary>
        /// <param name="sender">object</param>
        /// <param name="e">ASPxGridViewCustomCallbackEventArgs</param>
        protected void devGrid_CustomCallback(object sender, DevExpress.Web.ASPxGridViewCustomCallbackEventArgs e)
        {
            string[] oParams = e.Parameters.Split('|');

            if (oParams[0].Equals("SEARCH"))
            {
                devGrid.FilterExpression = "";
                // 업무지시목록
                WORK01_LST();
            }
            else if (oParams[0].Equals("DEL"))
            {
                List<string> oSPList = new List<string>();
                List<object> oParamList = new List<object>();

                foreach (string WORKID in oParams[1].Split(','))
                {
                    oParamDic = new Dictionary<string, string>();
                    oParamDic.Add("F_COMPCD", gsCOMPCD);
                    oParamDic.Add("F_FACTCD", gsFACTCD);
                    oParamDic.Add("F_WORKID", WORKID);
                    oParamDic.Add("F_USERID", gsUSERID);
                    oSPList.Add("USP_WORK01_DEL");
                    oParamList.Add((object)oParamDic);
                }

                #region Database Execute
                bool bExecute = false;
                string resultMsg = String.Empty;

                using (BSIFBiz biz = new BSIFBiz())
                {
                    bExecute = biz.PROC_QCD34_BATCH(oSPList.ToArray(), oParamList.ToArray(), out resultMsg);
                }

                if (!bExecute)
                {
                    procResult = new string[] { "0", String.Format("삭제 중 서버에서 장애가 발생하였습니다.\r계속해서 장애가 발행하는 경우 관리자에게 문의 바랍니다.\r\n에러내용 : {0}", resultMsg) };
                }
                else
                {
                    procResult = new string[] { "1", "삭제가 완료되었습니다." };

                    devGrid.FilterExpression = "";
                    // 업무지시목록
                    WORK01_LST();
                }

                devGrid.JSProperties["cpResultCode"] = procResult[0];
                devGrid.JSProperties["cpResultMsg"] = procResult[1];
                #endregion
            }
        }
        #endregion

        #endregion
    }
